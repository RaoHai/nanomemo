"""LLM judge for evaluating answer correctness."""

import json
import os
from typing import Literal

from openai import OpenAI

client = OpenAI(api_key=os.getenv("OPENAI_API_KEY"))

ACCURACY_PROMPT = """
Your task is to label an answer to a question as 'CORRECT' or 'WRONG'. You will be given:
    (1) a question (posed by one user to another user)
    (2) a 'gold' (ground truth) answer
    (3) a generated answer

The point of the question is to ask about something one user should know about the other user based on their prior conversations.

The gold answer will usually be concise and short. The generated answer might be much longer, but you should be generous with your grading - as long as it touches on the same topic as the gold answer, it should be counted as CORRECT.

For time-related questions, the gold answer will be a specific date, month, year, etc. The generated answer might use relative time references (like "last Tuesday" or "next month"), but you should be generous - as long as it refers to the same date or time period as the gold answer, it should be counted as CORRECT. Even if the format differs (e.g., "May 7th" vs "7 May"), consider it CORRECT if it's the same date.

Now it's time for the real question:
Question: {question}
Gold answer: {gold_answer}
Generated answer: {generated_answer}

First, provide a short (one sentence) explanation of your reasoning, then finish with CORRECT or WRONG.
Do NOT include both CORRECT and WRONG in your response.

Return the label in JSON format with the key "label" and value either "CORRECT" or "WRONG".
"""


def evaluate_llm_judge(
    question: str,
    gold_answer: str,
    generated_answer: str,
    model: str = "gpt-4o-mini",
) -> Literal[0, 1]:
    """
    Evaluate the generated answer against the gold answer using an LLM judge.

    Args:
        question: The question asked
        gold_answer: Ground truth answer
        generated_answer: Answer generated by the system
        model: OpenAI model to use for judging

    Returns:
        1 if CORRECT, 0 if WRONG
    """
    try:
        response = client.chat.completions.create(
            model=model,
            messages=[
                {
                    "role": "user",
                    "content": ACCURACY_PROMPT.format(
                        question=question,
                        gold_answer=gold_answer,
                        generated_answer=generated_answer,
                    ),
                }
            ],
            response_format={"type": "json_object"},
            temperature=0.0,
        )

        content = response.choices[0].message.content
        if not content:
            return 0

        result = json.loads(content)
        label = result.get("label", "WRONG")

        return 1 if label == "CORRECT" else 0

    except Exception as e:
        print(f"Error in LLM judge: {e}")
        return 0
